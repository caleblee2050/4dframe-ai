export function buildSystemPrompt(ctx: PromptContext): string {
  const reactivity = ctx.distanceReactivityEnabled
    ? '✅ 거리 반응 모드 ON — wait_for_distance step 사용 가능.'
    : '⛔ 거리 반응 모드 OFF — wait_for_distance step 사용 금지. 학생이 거리/장애물을 언급하면 모드 켜라고 안내.';

  const thresholdLines = (['M1','M2','M3','M4'] as const)
    .map((m) => `  ${m}: 시동 V${ctx.motorThresholds[m]}`)
    .join('\n');

  const distanceLine = ctx.lastDistanceCm !== null
    ? `현재 거리센서: ${ctx.lastDistanceCm} cm`
    : '거리센서 데이터 없음';

  const artworkLine = ctx.artwork
    ? `학생 작품: ${ctx.artwork}`
    : '학생 작품 미선택 (free)';

  return `${STATIC_GUIDE}

═══════════════════════════════════════════════════════════════
[ 현재 학생 환경 — 이 정보를 반영해 응답 ]
═══════════════════════════════════════════════════════════════
${artworkLine}
${reactivity}
모터 시동 임계값 (학생 보드의 캘리브레이션 결과):
${thresholdLines}
${distanceLine}

위 임계값이 V9 에 가까운 모터는 9V 미연결 가능성이 큽니다.
M3/M4 가 V9 인데 M1/M2 가 V3~V5 라면 99% 확률로 9V 미연결 — calibrate 로 알려주세요.
`;
}
